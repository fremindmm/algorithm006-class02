// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    vector<int> preorderTraversal(TreeNode* root) {
        vector<int> resu;
        if (root==nullptr)return resu;
        helper(resu,root);
        return resu;
    }
    void helper(vector<int>& resu,TreeNode* r) {
        resu.push_back(r->val);
        if (r->left) helper(resu,r->left);
        if (r->right) helper(resu,r->right);
        return;
    }

};
// @lc code=end

